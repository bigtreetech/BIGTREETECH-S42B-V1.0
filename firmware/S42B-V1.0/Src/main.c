//编码器校正函数
void CalibrateEncoder(void) 
{   
  int32_t encoderReading=0;    
  int32_t currentencoderReading=0;
  int32_t lastencoderReading=0;        

  int32_t iStart=0;    
  int32_t jStart=0;
  int32_t stepNo=0;
  
  int32_t fullStepReadings[200];//一圈200整步的编码器值数组
  int32_t ticks=0;	
  uint32_t address=0x08008000;//写入FLASH的起使地址

  uint16_t lookupAngle;
		
  dir=1; 
  Output(0,80);//开始时固定在整步位置
  for(uint8_t m=0;m<4;m++)
  {
    LED_H;
	LL_mDelay(250);
    LED_L;
	LL_mDelay(250);	
  } 
  for(int16_t x=0;x<=199;x++)//逆时针跑一圈记下各整步位的值
  {    
    encoderReading=0;
   	LL_mDelay(20);                     
    lastencoderReading=ReadAngle();     
    for(uint8_t reading=0;reading<10;reading++) 
	{ 
      currentencoderReading=ReadAngle(); 
      if(currentencoderReading-lastencoderReading<-8192)
        currentencoderReading+=16384;
      else if(currentencoderReading-lastencoderReading>8192)
        currentencoderReading-=16384;
 
      encoderReading+=currentencoderReading;
      LL_mDelay(10);
      lastencoderReading=currentencoderReading;
    }
    encoderReading=encoderReading/10;
    if(encoderReading>16384)
      encoderReading-=16384;
    else if(encoderReading<0)
      encoderReading+=16384;
    fullStepReadings[x]=encoderReading;  
    OneStep();
	LL_mDelay(100); 
  }
  dir=0; 
  OneStep();
  LL_mDelay(1000); 
  for(int16_t x=199;x>=0;x--)//顺时针跑一圈记下各整步位的值和之前逆时针保存的值求平均
  {    
    encoderReading=0;
   	LL_mDelay(20);                     
    lastencoderReading=ReadAngle();     
    for(uint8_t reading=0;reading<10;reading++) 
	{ 
      currentencoderReading=ReadAngle(); 
      if(currentencoderReading-lastencoderReading<-8192)
        currentencoderReading+=16384;
      else if(currentencoderReading-lastencoderReading>8192)
        currentencoderReading-=16384;
 
      encoderReading+=currentencoderReading;
      LL_mDelay(10);
      lastencoderReading=currentencoderReading;
    }
    encoderReading=encoderReading/10;
    if(encoderReading>16384)
      encoderReading-=16384;
    else if(encoderReading<0)
      encoderReading+=16384;
    fullStepReadings[x]=(fullStepReadings[x]+encoderReading)/2;  
    OneStep();
	LL_mDelay(100); 
  }
  LL_TIM_OC_SetCompareCH1(TIM3,0);  
  LL_TIM_OC_SetCompareCH2(TIM3,0); 
  for(uint8_t i=0;i<200;i++)//算出200整步间的插值数
  {
    ticks=fullStepReadings[(i+1)%200]-fullStepReadings[i%200];
    if(ticks<-15000) 
      ticks+=16384;
    else if(ticks>15000)	
	  ticks-=16384;	
    for(int32_t j=0;j<ticks;j++) 
	{
      stepNo=(fullStepReadings[i]+j)%16384;
      if(stepNo==0) 
      {
        iStart=i;
        jStart=j;
      }
    }
  }
  FlashUnlock();
  FlashErase32K();
  for(int32_t i=iStart;i<(iStart+200+1);i++)//以步进电机整步为基准（小于0.08度）开始对编码器进行校正插值并将校正后的值存入FLASH
  {
	ticks=fullStepReadings[(i+1)%200]-fullStepReadings[i%200];
    if(ticks<-15000) 
      ticks+=16384;         
    if(i==iStart) 
	{ 
      for(int32_t j=jStart;j<ticks;j++) 
	  {
        lookupAngle=(8192*i+8192*j/ticks)%1638400/100;
		FlashWriteHalfWord(address,(uint16_t)lookupAngle);
		address+=2;
      }
    }
    else if(i==(iStart+200)) 
	{ 
      for(int32_t j=0;j<jStart;j++) 
	  {
        lookupAngle=((8192*i+8192*j/ticks)%1638400)/100;
		FlashWriteHalfWord(address,(uint16_t)lookupAngle);
		address+=2;
      }
    }
    else 
	{                        //this is the general case
      for(int32_t j=0;j<ticks;j++) 
      {
        lookupAngle=((8192*i+8192*j/ticks)%1638400)/100;
		FlashWriteHalfWord(address,(uint16_t)lookupAngle);
		address+=2;
      }
    }
  }
  FlashLock();
  if(Second_Calibrate_flag !=1){                            //如果是二次校准就不存储flash
    //写flash 标记已校准过一次 ，待添加
    flash_store_flag =1;            //标记flash更新
    table1[0] =0xAA;                //标记已校准过
    table1[1] =128;table1[2] =16;
    table1[3] =4;table1[4] =3;
    table1[5] =0;table1[6] =1;
    table1[7] =1;table1[8] =1;
    table1[11]=kp;                  //校准前存储PID参数，最好把PID参数调好后写存储
    table1[12]=ki;
    table1[13]=kd;
  
    Calibration_flag =0xAA;    //标记已校准标志
//    Second_Menu=1;             //标记进入二级子菜单 
    STMFLASH_Write(Data_Store_Arrdess,table1,14);//存储数据
  }
  
  CalibrateEncoder_finish_flag=1; //标记校准完成  
}
