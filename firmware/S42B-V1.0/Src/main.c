{
  int16_t temp;
  temp=xMod%mMod;
  if(temp<0)
    return (temp+mMod);
  else
	return  temp;
}

void UsDelay(uint16_t us)
{
  us*=10;
  while(us)	
  {
    __NOP();
	__NOP();
	us--;
  }
}
